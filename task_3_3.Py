# Текст задания: 3. Написать функцию thesaurus(), принимающую в качестве
# аргументов имена сотрудников и возвращающую словарь
# , в котором ключи — первые буквы имен, а значения — списки, содержащие имена,
# начинающиеся с соответствующей буквы. Например:
# >>> >>> thesaurus("Иван", "Мария", "Петр", "Илья")
# {
#     "И": ["Иван", "Илья"],
#     "М": ["Мария"], "П": ["Петр"]
# }
# Подумайте: полезен ли будет вам оператор распаковки?
# Сможете ли вы вернуть отсортированный по ключам словарь?

def thesaurus(*args):
    name_list = {}
    for name in args:
        if name[0] in name_list.keys():
            name_list[name[0]].append(name)
        else:
            name_list[name[0]] = [name]

    print(name_list)

thesaurus("Иван", "Мария", "Сергей", "Петр", "Илья", "Игорь")

list_name = ['Иван', 'Игоррь', 'Илья', 'Дима', 'Михаил']
list_name.sort()
thesaurus(*list_name)
